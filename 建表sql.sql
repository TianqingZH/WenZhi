CREATE DATABASE wenzhi;
USE wenzhi;
CREATE TABLE member(
	#id ,用户标识id,密码，昵称，性别，头像，签名，创建时间
	id INT(5) NOT NULL AUTO_INCREMENT,
	memId VARCHAR(20) NOT NULL,
	pass  VARCHAR(20) NOT NULL,
	nickname VARCHAR(20) NOT NULL,
	sex VARCHAR(2) NOT NULL DEFAULT '男',
	tx STRING NOT NULL,
	sig VARCHAR(20) DEFAULT '0',
	mctime DATETIME NOT NULL,
	PRIMARY KEY(id),
	UNIQUE (memId)
	
);

CREATE TABLE topic(
	#id，主题id,创建时间，修改时间，用户id
	id INT(5) NOT NULL AUTO_INCREMENT,
	topId VARCHAR(20) NOT NULL,
	topCon VARCHAR(50) NOT NULL,
	ctime DATETIME NOT NULL,
	mtime DATETIME,
	memId VARCHAR(20) NOT NULL,
	PRIMARY KEY(id),
	UNIQUE (topId)
);


CREATE TABLE answer(
	#id，回答id，赞数，评论，评论数，创建时间，用户id,主题id
	id INT(20) NOT NULL AUTO_INCREMENT,
	answerId VARCHAR(20) NOT NULL,
	ansCon VARCHAR(50) NOT NULL,
	zan INT(4) NOT NULL DEFAULT '0',
	comCount INT(4) NOT NULL DEFAULT '0',
	ctime DATETIME NOT NULL,
	memId VARCHAR(20) NOT NULL,
	topId VARCHAR(20) NOT NULL,
	PRIMARY KEY(id),
	UNIQUE(answerId)
);

CREATE TABLE comm(
	#id，评论id,评论内容，创建时间，问题id，用户id
	id INT(20) NOT NULL AUTO_INCREMENT,
	commId VARCHAR(20) NOT NULL,
	commCon VARCHAR(20) NOT NULL,
	ctime DATETIME NOT NULL,
	answerId VARCHAR(20) NOT NULL,
	memId VARCHAR(20) NOT NULL,
	PRIMARY KEY(id),
	UNIQUE(commId)
);
CREATE TABLE dyna(
	#用户动态表，
	#id，动态id，用户id，创建时间，answerId,flag，0:赞同了回答，1:评论了回答,3:回答了主题
	id INT(20) NOT NULL AUTO_INCREMENT,
	dynaId VARCHAR(20) NOT NULL,
	memId VARCHAR(20) NOT NULL,
	ctime DATETIME NOT NULL,
	otherId VARCHAR(20) NOT NULL,
	flag VARCHAR(20) NOT NULL,
	PRIMARY KEY(id),
	UNIQUE(dynaId)
);
